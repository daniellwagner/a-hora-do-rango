    private void itemDeMenuEstabelecimentosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemDeMenuEstabelecimentosActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        int returnVal = fileChooser.showOpenDialog(this);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            GUIEstabelecimento guiEstabelecimentos = new GUIEstabelecimento();
            File file = fileChooser.getSelectedFile();
            try {
              // What to do with the file, e.g. display it in a TextArea
            //tabelaEstabelecimentos.read( new FileReader( file.getAbsolutePath() ), null );
            desktop.add(guiEstabelecimentos);
            guiEstabelecimentos.setVisible(true);
            
            
            } catch (Exception ex) {
              System.out.println("problem accessing file"+file.getAbsolutePath());
            }
            
        } else {
            System.out.println("File access cancelled by user.");
        }

    }//GEN-LAST:event_itemDeMenuEstabelecimentosActionPerformed

